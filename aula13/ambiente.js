var c = 1;
var b = 1;

// Estrutura de repetição com teste lógico no início
while (c <= 6) {
  console.log(`Passo ${c}`);
  c++; // c = c + 1
}

// Estrutura de repetição com teste lógico no fim
do {
  console.log(`Passo ${b}`);
  b++; // c = c + 1
} while (b <= 6);

